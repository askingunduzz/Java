package cmpe113;

public class Escape {
	 public static void main(String[] args) {

		    System.out.println("Hi, how are \ryou?");
		    System.out.println("Hi, how are \nyou?");
		    System.out.println("Hi, how are \tyou?");
		    System.out.println("\"Hi, how are you?\"");
		    System.out.println("Hi, how are you\'?'");
		    System.out.println("Hi, how are you\\?");

		  }
}
